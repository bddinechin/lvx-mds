  properties=>  $properties,
  formats=>     &Format::IDs($formats),
  schedulings=> &Scheduling::IDs($schedulings),
  syntax=>      $syntax,
  forced=>      $forced,
  values=>      $values,
}, $children)->emit();\n
EOT
}


