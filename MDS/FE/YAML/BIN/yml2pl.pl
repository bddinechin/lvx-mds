my ($TableName) = reverse split '/', $ARGV[0];
$TableName =~ s/\.\w+$//;

my ($program) = reverse split '/', $0;
print <<"EOT";
# This file has been automatically generated by $program.

use $TableName;

EOT


local $/;
my $Load = Load(<>);
my $Table = $$Load{$TableName};
my @Table = sort { $$a{number} <=> $$b{number} } values %{$Table};
foreach my $entry (@Table) {
    #print STDERR "reading $TableName...\n";
    eval "$TableName(\$entry)";
}

sub attribute {
    my ($entry, $key) = @_;
    my $value = $$entry{$key};
    if (defined $value && length($value)) {
        if (ref $value eq 'ARRAY') {
            return 'undef' unless @{$value};
            $value = join ' ', @{$value};
            return "'$value'";
        } elsif (ref $value eq 'HASH') {
            my @values;
            while (my ($type, $name) = each %{$value}) {
                push @values, "&". $type. "::ID('$name')";
            }
            if (@values > 1) {
                return '[ '. (join ', ', @values). ' ]';
            }
            return $values[0];
        } else {
            return "'$value'";
        }
    }
    return 'undef';
}

sub properties {
    my ($entry, $key) = @_;
    my $value = $$entry{$key};
    if (defined $value) {
        if (ref $value eq 'HASH') {
            my @properties; map {
                my ($proxy, $sequence) = ($_, $$value{$_});
                push @properties, "'$proxy: $sequence'";
              } sort { substr($a,1) cmp substr($b,1) } keys %{$value};
            if (@properties > 1) {
                return '[ '. (join ', ', @properties). ' ]';
            }
            return $properties[0];
        } else {
            die "expecting a YAML map in 'properties' attribute";
        }
    }
    return 'undef';
}

sub execution {
    my ($entry) = @_;
    return 'undef' unless defined $entry;
    my $execution = $$entry{execution};
    return undef unless $execution =~ /\S/;
    $execution =~ s/\&/&amp;/g;
    $execution =~ s/\</&lt;/g;
    $execution =~ s/\>/&gt;/g;
    return <<"EOT";
MDS::make("Execution", { }, ['
$execution'])
EOT
}

