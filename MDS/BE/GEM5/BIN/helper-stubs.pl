#!/usr/bin/perl
# BE/GEM5 — generate helper_stubs.inc for the LVX gem5 port.
#
# The MDS-generated behavior bodies (Behavior.tuple) call operator HELPERs that
# the gem5 port does not implement yet; each such helper gets a uniform panic
# stub here so the behavior translation unit links. Helpers that the runtime
# shim (shim.cc) actually implements are listed in the family shim-helpers
# manifest and excluded, so the shim's definitions are the ones that link.
#
# Usage: helper-stubs.pl <shim-helpers-manifest> <Behavior.tuple> > helper_stubs.inc
#
# Each declared helper appears in Behavior.tuple as:
#   BehaviorDeclare(NAME,DECLARE(<ret> HELPER(NAME)(<params>);))
# We turn the extern DECLARE variant into "<proto> { lvx_behavior_unimpl(); }".

use strict;
use warnings;

my ($manifest, $tuple) = @ARGV;
die "usage: $0 <shim-helpers> <Behavior.tuple>\n" unless defined $tuple;

# Read the manifest of shim-implemented helpers to exclude.
my %implemented;
open(my $m, '<', $manifest) or die "cannot open $manifest: $!";
while (<$m>) {
    s/#.*//;                 # strip comments
    s/^\s+|\s+$//g;          # trim
    next if $_ eq '';
    $implemented{$_} = 1;
}
close($m);

# Extract the extern DECLARE prototype of each helper, skip the implemented
# ones, and emit a panic stub. Dedup and sort for a stable output.
my %stub;
open(my $t, '<', $tuple) or die "cannot open $tuple: $!";
while (<$t>) {
    next unless /^BehaviorDeclare\(([^,]+),DECLARE\((.*);\)\)\s*$/;
    my ($name, $proto) = ($1, $2);
    next if $implemented{$name};
    $stub{"$proto { lvx_behavior_unimpl(); }"} = 1;
}
close($t);

print "/* Auto-generated by MDS BE/GEM5 (helper-stubs.pl). Do not edit. */\n";
print "$_\n" for sort keys %stub;
