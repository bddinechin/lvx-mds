# BE/GEM5 -- generate the LVX pipeline-stage enum for the gem5 port.
#
# The Processor's `pipeline` names each stage and `stages` gives the cycle at
# which it acts (Processor.table: pipeline="ID RR E1 E2 CR E3 E4 E5 WB SF SR",
# stages="0 1 2 3 4 4 5 6 6 16 24").  Operands are read at RR and each result is
# written at its commit stage, so a result's dependence latency is its write
# stage minus LVX_STAGE_RR.  Emitting the stages as a named enum keeps the
# latency constants used by LvxStaticInst and the MinorCPU FU pool out of the
# code as literals -- one source of truth, the ISA description.
#
# Usage: pipeline-stages.pl [--py] <Processor.table> > lvx_stages.{h,py}

use strict;
use warnings;

my $py = 0;
if (@ARGV && $ARGV[0] eq '--py') { $py = 1; shift @ARGV; }
my ($table) = @ARGV;
die "usage: $0 [--py] <Processor.table>\n" unless defined $table;

open(my $f, '<', $table) or die "cannot open $table: $!";
my ($names, $stages);
while (<$f>) {
    $names  = $1 if /pipeline=\s*"([^"]*)"/;
    $stages = $1 if /stages=\s*"([^"]*)"/;
}
close($f);
die "no pipeline in $table\n" unless defined $names && defined $stages;
my @n = split ' ', $names;
my @s = split ' ', $stages;
die "pipeline/stages length mismatch in $table\n" unless @n == @s;

if ($py) {
    print "# Auto-generated by MDS BE/GEM5 (pipeline-stages.pl). Do not edit.\n";
    print "#\n# LVX pipeline stages: the cycle each stage acts at. A result's\n";
    print "# dependence latency is its write stage minus LVX_STAGE_RR.\n\n";
    print "LVX_STAGE_$n[$_] = $s[$_]\n" for 0 .. $#n;
} else {
    print "/* Auto-generated by MDS BE/GEM5 (pipeline-stages.pl). Do not edit.\n";
    print " *\n";
    print " * LVX pipeline stages: the cycle each stage acts at. Operands are read\n";
    print " * at LVX_STAGE_RR and each result written at its commit stage, so a\n";
    print " * result's dependence latency is (write stage - LVX_STAGE_RR). */\n";
    print "#ifndef __ARCH_LVX_GENERATED_LVX_STAGES_H__\n";
    print "#define __ARCH_LVX_GENERATED_LVX_STAGES_H__\n\n";
    print "enum LvxStage\n{\n";
    printf "    LVX_STAGE_%-3s = %2d,\n", $n[$_], $s[$_] for 0 .. $#n;
    print "};\n\n#endif /* __ARCH_LVX_GENERATED_LVX_STAGES_H__ */\n";
}
