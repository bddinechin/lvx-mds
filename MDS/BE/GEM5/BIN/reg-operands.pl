# BE/GEM5 — generate reg_operands.inc for the LVX gem5 port.
#
# The gem5 timing CPUs (Minor, O3) build their dependency graph from each
# StaticInst's source/destination register lists.  The MDS behavior bodies are
# the authority on which registers an opcode reads and writes, but they express
# it operationally: the FETCH body reads every source, the COMMIT body writes
# every destination, through the runtime shim's register helpers.  This turns
# that into a static per-opcode table LvxStaticInst can consult at decode time.
#
# Usage: reg-operands.pl <Behavior.tuple> > reg_operands.inc
#
# In a FETCH body a source register appears as either
#   HELPER(operandFromRegFile_<FILE>)(this, stage, rank, slot, decoded[K] - Register_<...>);
#     -- an operand register: file <FILE>, index = decoded[K] - <file base>;
#   HELPER(readFromStorage_<FILE>)(this, stage, <num>, ...);
#     -- a fixed/implicit register: file <FILE>, index = <num> (e.g. $ra = SFR 3).
# A COMMIT body writes destinations the same way with operandToRegFile_<FILE> /
# writeToStorage_<FILE>.  PC/NPC/PS storages are control/state handled by the
# gem5 PCState, not register files, so they are skipped here.

use strict;
use warnings;

my ($tuple) = @ARGV;
die "usage: $0 <Behavior.tuple>\n" unless defined $tuple;

# Register files we model as gem5 register classes.  Anything else (PC, NPC,
# PS, memory) is not a scoreboard register here and is ignored.
my %REGFILE = (GPR => 1, SFR => 1);

# Parse every fetch_/execute_/commit_ body once, collecting its register
# accesses split into reads and writes.  A source may be read in any phase: an
# operand register is loaded in FETCH (operandFromRegFile), but a fixed/implicit
# register is often read in EXECUTE (e.g. RET reads $ra = SFR 3 there to compute
# the branch target).  So classify by the helper's direction, not the phase.
# Many opcodes share a body, so key by function name and resolve per opcode next.
my (%rd, %wr);            # fn name => [ "file:slot" | "file:#num", ... ]
my $fn;
open(my $t, '<', $tuple) or die "cannot open $tuple: $!";
while (<$t>) {
    if (/^(?:fetch|execute|commit)_(lvx_\w+?)\(/) {   # function definition line
        $fn = $&; $fn =~ s/\($//;
        $rd{$fn} = []; $wr{$fn} = [];
        next;
    }
    next unless defined $fn;
    if (/^\}/) { $fn = undef; next; }                 # end of body

    # Operand register: index carried by decoded[K].
    if (/operand(From|To)RegFile_(\w+)\)\(this,[^,]+,[^,]+,[^,]+,\s*decoded\[(\d+)\]/) {
        next unless $REGFILE{$2};
        push @{ $1 eq 'From' ? $rd{$fn} : $wr{$fn} }, "$2:$3";
    }
    # Fixed/implicit register: a literal storage cell number.
    elsif (/(read|write)FromStorage_(\w+)\)\(this,[^,]+,\s*(\d+)\s*,/) {
        next unless $REGFILE{$2};
        push @{ $1 eq 'read' ? $rd{$fn} : $wr{$fn} }, "$2:#$3";
    }
}
close($t);

# Resolve the dispatch: an opcode reads the union of the reads of its three
# bodies, and writes the union of their writes (deduped, order preserved).
sub uniq { my %s; grep { !$s{$_}++ } @_ }
my %src;   # opcode enum => [accesses]
my %dst;
open($t, '<', $tuple) or die "cannot open $tuple: $!";
while (<$t>) {
    next unless /^Behavior\((\w+),FETCH\((\w+)\),EXECUTE\((\w+)\),COMMIT\((\w+)\)\)/;
    my ($opcode, $fetch, $execute, $commit) = ($1, $2, $3, $4);
    $src{$opcode} = [ &uniq(map { @{$rd{$_} || []} } $fetch, $execute, $commit) ];
    $dst{$opcode} = [ &uniq(map { @{$wr{$_} || []} } $fetch, $execute, $commit) ];
}
close($t);

# Emit.  Dedup identical operand lists into shared arrays, then a table keyed by
# the Opcode enum.  An access "FILE:slot" reads decoded[slot]; "FILE:#num" is the
# fixed register `num`.  The port turns FILE into a gem5 RegClass and computes
# the index (decoded[slot] - file base, or num).
my %arrayId;   # serialized list => C identifier
my @arrays;    # [id, [accesses]]
sub intern {
    my ($list) = @_;
    my $key = join(',', @$list);
    return 'lvx_regops_none' if $key eq '';
    unless (exists $arrayId{$key}) {
        my $id = 'lvx_regops_' . scalar(@arrays);
        $arrayId{$key} = $id;
        push @arrays, [$id, $list];
    }
    return $arrayId{$key};
}

# Pre-intern so shared bodies collapse.
my (%opSrc, %opDst);
for my $op (sort keys %src) {
    $opSrc{$op} = &intern($src{$op});
    $opDst{$op} = &intern($dst{$op});
}

print <<'HEAD';
/* Auto-generated by MDS BE/GEM5 (reg-operands.pl). Do not edit.
 *
 * Per-opcode source and destination register operands, for gem5 dependency
 * tracking (LvxStaticInst builds _srcRegIdx/_destRegIdx from this).  Each entry
 * names a register file and how to find the register index: `slot >= 0` reads
 * decoded[slot] (an operand register, index = decoded[slot] - file base);
 * `slot < 0` is a fixed register whose number is `fixed`.
 */
#ifndef LVX_REGOP
/* file codes */
#define LVX_RF_GPR 0
#define LVX_RF_SFR 1
typedef struct { unsigned char file; signed char slot; unsigned short fixed; } LvxRegOperand;
#endif

HEAD

my %FILECODE = (GPR => 'LVX_RF_GPR', SFR => 'LVX_RF_SFR');
sub emit_entry {
    my ($acc) = @_;
    my ($file, $rest) = split /:/, $acc, 2;
    if ($rest =~ /^#(\d+)$/) { return "{ $FILECODE{$file}, -1, $1 }"; }
    return "{ $FILECODE{$file}, $rest, 0 }";
}

print "static const LvxRegOperand lvx_regops_none[1] = { { 0, 0, 0 } };\n";
for my $a (@arrays) {
    my ($id, $list) = @$a;
    print "static const LvxRegOperand ${id}[] = { ",
          join(', ', map { &emit_entry($_) } @$list), " };\n";
}

print "\n#ifndef LVX_REGOP\ntypedef struct { const LvxRegOperand *src; unsigned char nsrc;",
      " const LvxRegOperand *dst; unsigned char ndst; } LvxRegDeps;\n#endif\n\n";
# External linkage (defined in behavior.c, compiled as C so the designated array
# initializer is standard): LvxStaticInst reads it through reg_operands.hh.
print "const LvxRegDeps lvx_reg_deps[Opcode__NUM] = {\n";
for my $op (sort keys %src) {
    my $ns = scalar @{$src{$op}};
    my $nd = scalar @{$dst{$op}};
    printf "  [Opcode_%s] = { %s, %d, %s, %d },\n",
        $op, $opSrc{$op}, $ns, $opDst{$op}, $nd;
}
print "};\n";
