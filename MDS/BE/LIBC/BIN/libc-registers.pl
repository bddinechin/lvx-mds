# Emits newlib's mbr/<family>/registers.h: SFR index and bit-field
# constants for every Special Function Register, so that libc/libgloss
# no longer hand-ports a subset of these from the ISA description.

my $debug = $ENV{DEBUG} || 0;
my $family = lc($ENV{FAMILY});
my $FAMILY = uc($family);

use Carp;
use strict;
use Data::Dumper;

use MDS;
&MDS::parse(*ARGV);
use integer;

print << "EOT";
/**
 * \@file registers.h
 * \@brief Special Function Register (SFR) index and bit-field constants
 *
 * \@section LICENSE
 *  Copyright (C) 2009-2026 Kalray SA.
 *  All rights reserved.
 *
 * Generated by MDS/BE/LIBC/BIN/libc-registers.pl from the $family ISA
 * description (Register.table, RegField.table). Do not edit by hand;
 * regenerate instead.
 */

#ifndef _${FAMILY}_REGISTERS_H
#define _${FAMILY}_REGISTERS_H

EOT

# Collect every SFR that has a "\$sNN" alias: that alias is the operand
# accepted by __builtin_${family}_get/set/wfxl, and is what the hand-ported
# subset of this file used to key off (see git history).
my @Registers;
foreach my $register (@Register::table) {
    my ($regFile) = $register->access("regFile");
    next unless defined $regFile && $regFile->name() eq "SFR";
    my $dwarfId = $register->attribute("dwarfId");
    next unless defined $dwarfId;
    my $names = $register->attribute("names") || '';
    my ($sfrAlias) = grep { /^\$s\d+$/ } split ' ', $names;
    next unless defined $sfrAlias;
    my ($sfrIndex) = $sfrAlias =~ /^\$s(\d+)$/;
    push @Registers, { REGISTER=>$register, DWARFID=>$dwarfId, SFRINDEX=>$sfrIndex,
                        NAME=>$register->name(), ALIAS=>(split ' ', $names)[0] };
}
@Registers = sort { $a->{SFRINDEX} <=> $b->{SFRINDEX} } @Registers;

print "/* SFR indices (operand to __builtin_${family}_get/set/wfxl) */\n";
foreach my $entry (@Registers) {
    my $NAME = uc($entry->{NAME});
    printf "#define %s_SFR_%-16s %3d  // %s \$s%d\n",
      $FAMILY, $NAME, $entry->{SFRINDEX}, $entry->{ALIAS}, $entry->{SFRINDEX};
}
print "\n";

foreach my $entry (@Registers) {
    my $register = $entry->{REGISTER};
    my @fields = $register->access("fields");
    next unless @fields;
    my $NAME = uc($entry->{NAME});
    print "/* $entry->{NAME} bit-field offsets */\n";
    foreach my $field (@fields) {
        my $fieldName = uc($field->name());
        my $offset = $field->attribute("offset");
        my $width = $field->attribute("width");
        print "#define ${FAMILY}_SFR_${fieldName}_SHIFT      $offset\n";
        print "#define ${FAMILY}_SFR_${fieldName}_WIDTH      $width\n";
    }
    print "\n";
}

# __builtin_${family}_wfxl(sfr, mask) packs a clear-mask in the low 32 bits
# and a set-value (pre-shifted into the same bit position + 32) in the high
# 32 bits of its second operand. Only emitted for single-bit fields, where
# "set" is unambiguous; multi-bit fields need a caller-supplied value and
# get SHIFT/WIDTH only (above).
foreach my $entry (@Registers) {
    my $register = $entry->{REGISTER};
    my @fields = grep { $_->attribute("width") == 1 } $register->access("fields");
    next unless @fields;
    print "/* $entry->{NAME} __builtin_${family}_wfxl() masks */\n";
    foreach my $field (@fields) {
        my $fieldName = uc($field->name());
        my $offset = $field->attribute("offset");
        my $mask = 1 << $offset;
        printf "#define %s_SFR_%s_WFXL_MASK  0x%xLL\n", $FAMILY, $fieldName, $mask;
        printf "#define %s_SFR_%s_WFXL_CLEAR 0x%xLL\n", $FAMILY, $fieldName, $mask;
        printf "#define %s_SFR_%s_WFXL_SET   0x%xLL\n", $FAMILY, $fieldName, $mask << 32;
    }
    print "\n";
}

print "#endif /* _${FAMILY}_REGISTERS_H */\n";

