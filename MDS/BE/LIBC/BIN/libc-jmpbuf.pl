# Emits <family>-jmpbuf.h: the setjmp/longjmp register-save layout, as a
# single source shared by newlib's setjmp.S (which writes/reads it) and
# gdb's lvx-common-tdep.c (which needs the RA offset to implement
# "until"/get_longjmp_target). Before this backend existed, both sides
# hand-encoded the same layout with only a source comment cross-referencing
# the other file - this generates it from Convention-lvx-regular instead,
# so a change to the callee-saved register set cannot silently desync them.

my $debug = $ENV{DEBUG} || 0;
my $family = lc($ENV{FAMILY});
my $FAMILY = uc($family);

use Carp;
use strict;
use Data::Dumper;

use MDS;
&MDS::parse(*ARGV);
use integer;

# There should be exactly one calling convention for this family/core; use
# the first one, same policy as gcc-registers.pl's ABI section.
my ($convention) = @Convention::table;
die "No Convention found\n" unless defined $convention;

my @callee = map { $_->name() } $convention->access("callee");
my ($frame) = map { $_->name() } $convention->access("frame");
my ($local) = map { $_->name() } $convention->access("local");
my ($stack) = map { $_->name() } $convention->access("stack");
my ($return) = map { $_->name() } $convention->access("return");

# Registers preserved across setjmp/longjmp beyond the plain ABI callee-saved
# GPR set: the hardware loop registers, which are architectural state but
# are not part of Convention-lvx-regular (that table only covers the
# general-purpose calling convention, not the loop unit). This list is a
# deliberate choice of this runtime's jmp_buf layout, not MDS-derived data;
# update it here if the loop unit's SFR set ever changes.
my @extra = ('CS', 'LS', 'LE', 'LC');

# Registers are saved/restored 2-or-4-at-a-time (sq/so instructions need
# contiguous register numbers), in the grouping below. This grouping is a
# jmp_buf packing policy (not something Convention.table encodes), but the
# *set* of registers it covers is checked against @callee/$frame/$local
# below so a change to the ABI's callee-saved set fails loudly here instead
# of silently desyncing setjmp.S from gdb's lvx-common-tdep.c.
my @groups = (
    [ 'R18', 'R19' ],
    [ 'R20', 'R21', 'R22', 'R23' ],
    [ 'R24', 'R25', 'R26', 'R27' ],
    [ 'R28', 'R29', 'R30', 'R31' ],
    [ $local, $frame, 'LS', 'LE' ],
    [ 'LC', 'CS', $return, $stack ],
);

# Sanity-check: every callee-saved GPR (and the frame/local pointers) must
# appear in @groups exactly once, and @groups must not invent registers
# Convention.table doesn't know about.
my %expected = map { $_ => 1 } (@callee, $frame, $local);
my %grouped;
foreach my $group (@groups) {
    foreach my $reg (@$group) {
        next if grep { $reg eq $_ } @extra;
        next if $reg eq $return || $reg eq $stack;
        die "Register $reg appears twice in the jmp_buf layout\n" if $grouped{$reg}++;
        die "Register $reg is saved by setjmp but is not in Convention-lvx-regular's callee/frame/local\n"
          unless $expected{$reg};
    }
}
foreach my $reg (keys %expected) {
    die "Callee-saved register $reg has no slot in the jmp_buf layout\n" unless $grouped{$reg};
}

print << "EOT";
/*
 * ${family}-jmpbuf.h: setjmp/longjmp register-save layout.
 *
 * Generated by MDS/BE/LIBC/BIN/libc-jmpbuf.pl from Convention-${family}-regular
 * (Convention.table). Do not edit by hand; regenerate instead. Consumed by
 * newlib's setjmp.S and gdb's lvx-common-tdep.c (get_longjmp_target).
 */

#ifndef _${FAMILY}_JMPBUF_H
#define _${FAMILY}_JMPBUF_H

EOT

my $offset = 0;
foreach my $group (@groups) {
    foreach my $reg (@$group) {
        printf "#define %s_JMPBUF_%s_OFFSET  0x%02x\n", $FAMILY, $reg, $offset;
        $offset += 8;
    }
}
print "\n#define ${FAMILY}_JMPBUF_SIZE          0x" . sprintf("%02x", $offset) . "\n";
print "\n#endif /* _${FAMILY}_JMPBUF_H */\n";

